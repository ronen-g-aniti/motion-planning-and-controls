class Battery:
	def __init__(self, capacity_in_mah: float):
		self.capacity_in_mah = capacity_in_mah
		self.charge_in_mah = self.capacity_in_mah 
	def lose_charge(charge_to_lost_in_mah: float) -> None:
		self.charge_in_mah -= charge_to_lose_in_mah
	def recharge():
		self.charge_in_mah = self.capacity_in_mah 
